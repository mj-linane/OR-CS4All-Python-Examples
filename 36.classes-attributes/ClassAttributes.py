# OBJECT ORIENTED PROGRAMMING AND CLASSES -- Class Attributes

class Dog(object):

    def __init__(self):
        print("Hello! I am new here.")

    def say_hello(self):
        print("Hello! I am an instance of a dog. I am new here")

    def bark(self):
        print("Woof!!!")

    def sit(self):
        print("I am sitting")


